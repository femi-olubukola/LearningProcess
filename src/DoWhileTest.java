//a do…while to output the numbers 1–10

// do...while repetition statement.

public class DoWhileTest {

    public static void main(String[] args) {

        int counter = 1;

        do {
            System.out.print(counter + " ");
            counter++;
        } while (counter <= 10); // end do...while

        System.out.println();
    }
}
